#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	j;

	i = 0;
	j = (char)c;
	while (s[i])
		i++;
	while (s[i] != j && i >= 0)
		i--;
	return (s[i] == c ? (char *)&s[i] : 0);
}
